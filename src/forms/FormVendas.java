    //recebe o Produto e a qtde lá do FormPesquisarProduto e insere na tblItens
    public void recebeProduto(Produto p, int quantidade) {
        int numeroLinha = verificarItemDuplicado(p.getId());
        if(numeroLinha == -1) {
            //Produto novo, add uma nova linha
            DefaultTableModel tabela = (DefaultTableModel) tblItens.getModel();
            Object[] novaLinha = new Object[]{
                p.getId(),
                p.getNomeproduto(),
                p.getValor(),
                quantidade,
                p.getValor() * quantidade
            };
            tabela.addRow(novaLinha);
        }
        else {
            //Já tem o produto na tabela, então só altera a quantidade
            int qtdeAtual = Integer.parseInt(tblItens.getValueAt(numeroLinha, 3).toString());
            int novaQtde = qtdeAtual + quantidade;
            double novoTotal = novaQtde * p.getValor();
            
            tblItens.setValueAt(novaQtde, numeroLinha, 3);
            tblItens.setValueAt(novoTotal, numeroLinha, 4);
        }
        calcularValorTotal();
    }
    
    //retorna a qtde atual do item na tblItens (para ser usado no calculo do estoque)
    public int getQuantidadeItem(int id) {
        int quantidade = 0;
        for(int i = 0; i < tblItens.getRowCount(); i++) {
            int idAtual = Integer.parseInt(tblItens.getValueAt(i, 0).toString());
            if(idAtual == id) {
                //encontrou o produto, pegue a quantidade dele
                quantidade = Integer.parseInt(tblItens.getValueAt(i, 3).toString());
                break;
            }
        }
        return quantidade;
    }
    
    //retorna o número da linha do produto duplicado
    //ou -1 se ainda nao existe na tabela
    private int verificarItemDuplicado(int id) {
        int numeroLinha = -1;
        for(int i = 0; i < tblItens.getRowCount(); i++) {
            //verifica se o id da linha atual (i) é igual ao id que veio como param.
            int idAtual = Integer.parseInt(tblItens.getValueAt(i, 0).toString());
            if(idAtual == id){
                numeroLinha = i;
                break; //interrompe o laço, pq já encontrou o que estava procurando
            }
        }
        return numeroLinha;
    }
    
    private void calcularValorTotal() {
        double valorTotal = 0;
        //percorre a tblItens e vai somando todos os "totais"
        for(int i = 0; i < tblItens.getRowCount(); i++) {
            double totalItem = Double.parseDouble(tblItens.getValueAt(i, 4).toString());
            valorTotal += totalItem;
        }
        txtValorTotal.setText(String.valueOf(valorTotal));
    }

