    private void btnRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverItemActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblItens.getSelectedRow();
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Selecione uma linha na tabela");
        }
        else{
            DefaultTableModel tabela = (DefaultTableModel) tblItens.getModel();
            tabela.removeRow(linhaSelecionada);
            calcularValorTotal();
        }
    }//GEN-LAST:event_btnRemoverItemActionPerformed

    private void btnConcluirVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirVendaActionPerformed
        // TODO add your handling code here:
        //validar se existe pelo menos 1 item na tabela
        int countTabela = tblItens.getRowCount();
        if(countTabela == 0){
            JOptionPane.showMessageDialog(this, "Adicione pelo menos 1 produto na tabela");
        }
        else{
            //Cria o cliente
            Cliente c = new Cliente();
            c.setId(Integer.parseInt(txtIdCliente.getText()));
            
            //Cria a lista de Itens e pega os itens na tabela
            List<Itens> itens = new ArrayList();
            for(int x = 0; x < countTabela; x++) {
                Itens i = new Itens();
                //pega o produto(item) e a quantidade
                Produto p = new Produto();
                p.setId(Integer.parseInt(
                    tblItens.getValueAt(x, 0).toString()
                ));
                
                i.setProduto(p);
                i.setQuantidade(Integer.parseInt(
                    tblItens.getValueAt(x, 3).toString()
                ));
                
                //add o i na lista
                itens.add(i);
            }
            
            //Por fim, crie a venda
            Venda v = new Venda();
            v.setDatavenda(txtDataVenda.getText());
            v.setValortotal(Double.parseDouble(txtValorTotal.getText()));
            v.setFormapagamento(cmbFormaPagamento.getSelectedItem().toString());
            
            v.setCliente(c);
            v.setItens(itens);
            
            //Salvar a venda
            VendaDAO vendaDAO = new VendaDAO();
            try {
                vendaDAO.inserir(v);
                JOptionPane.showMessageDialog(this, "Venda efetuada com sucesso!");
                this.dispose(); //fecha o form de venda
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnConcluirVendaActionPerformed

