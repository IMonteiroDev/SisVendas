    private void preencherTabela() {
        DefaultTableModel tabela = (DefaultTableModel) tblClientes.getModel();
        tabela.setNumRows(0);
        
        try {
            List<Cliente> lista = this.clienteDAO.getClientes();
            
            //percorre a lista e insere na jTable
            for(Cliente c: lista){
                Object[] novaLinha = new Object[]{
                    c.getId(),
                    c.getNomecliente()
                };
                tabela.addRow(novaLinha);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

