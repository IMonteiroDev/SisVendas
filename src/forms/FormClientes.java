    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String nomecliente = txtNomeCliente.getText();
        
        Cliente c = new Cliente();
        c.setNomecliente(nomecliente);
        
        try {
            this.clienteDAO.inserir(c);
            JOptionPane.showMessageDialog(this, "Cliente salvo com sucesso");
            limparCampos();
            preencherTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCarregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarClienteActionPerformed
        // TODO add your handling code here:
        //pegar a linha selecionada na tabela
        int linhaSelecionada = tblClientes.getSelectedRow();
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Selecione uma linha na tabela");
        }
        else{
            //pegar o código da linha (produto) selecionada
            String strId = tblClientes.getValueAt(linhaSelecionada, 0).toString();
            int id = Integer.parseInt(strId);
            
            try {
                Cliente c = this.clienteDAO.getCliente(id);
                
                txtId.setText(String.valueOf(c.getId()));
                txtNomeCliente.setText(c.getNomecliente());
                
                habilitarSalvar(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnCarregarClienteActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        //pegar os valores inseridos no formulário
        int id = Integer.parseInt(txtId.getText());
        String nomecliente = txtNomeCliente.getText();
        
        Cliente c = new Cliente();
        c.setId(id);
        c.setNomecliente(nomecliente);
        
        try {
            this.clienteDAO.editar(c);
            JOptionPane.showMessageDialog(this, "Cliente editado com sucesso");
            limparCampos();
            preencherTabela();
            habilitarSalvar(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int confirma = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir?");
        if(confirma == 0) {
            int id = Integer.parseInt(txtId.getText());
            
            try {
                this.clienteDAO.excluir(id);
                JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso");
                limparCampos();
                preencherTabela();
                habilitarSalvar(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

